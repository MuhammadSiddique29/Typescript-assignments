// Equality and inequality
let myCity = "Karachi"
console.log(myCity == "Karachi")
console.log(myCity !== "Karachi")


// Using lower case function
let myName = "Siddique"
console.log(myName !== "siddique")
console.log(myName == "siddique")


// Numerical test 
let myNum = 8
console.log(myNum == 8)
console.log(myNum !== 8)
console.log(myNum > 5)
console.log(myNum < 4)
console.log(myNum <= 3)
console.log(myNum >= 2)



// Test using and(&) and or(|)
let num1 = 10
let num2 = 20

console.log(num1 < 15 && num2 > 25)

console.log(num1 < 15 || num2 > 25)



